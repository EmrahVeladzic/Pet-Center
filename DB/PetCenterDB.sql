CREATE TRIGGER trg_delete_Comment
ON [Communication].[Comment]
AFTER DELETE
AS
BEGIN
    SET NOCOUNT ON;
    
    UPDATE [Communication].[Comment]
    SET ParentCommentID = NULL
    WHERE ParentCommentID IN (SELECT ID FROM deleted);
END
