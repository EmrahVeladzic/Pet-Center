CREATE TRIGGER trg_delete_User
ON [Person].[User]
AFTER DELETE 
AS
BEGIN
	SET NOCOUNT ON;
	
	UPDATE [T]
    SET [T].PosterID = NULL
    FROM [Communication].[Thread] [T]
    JOIN deleted [D] ON [T].PosterID = [D].ID;

    UPDATE [C]
    SET [C].PosterID = NULL
    FROM [Communication].[Comment] [C]
    JOIN deleted [D] ON [C].PosterID = [D].ID;

    DELETE [A]
    FROM [File].[Album] [A]
    JOIN deleted [D] ON [A].ID = [D].ProfilePictureID;
END
GO

